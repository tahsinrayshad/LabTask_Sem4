CREATE TABLE Author
(
name VARCHAR2(50),
ID NUMBER primary key
) TABLESPACE tsp1;

CREATE TABLE Publisher
(
ID NUMBER primary key ,
name VARCHAR2(50)
) TABLESPACE tsp1;
--4)Create another table book(ISBN_code, name, price, fk[publisher_ID]) in tsp2.
CREATE TABLE book
(
ISBN_code NUMBER,
name VARCHAR2(50),
price NUMBER,
publisher_ID NUMBER,
FOREIGN KEY (publisher_ID) references Publisher(ID)
